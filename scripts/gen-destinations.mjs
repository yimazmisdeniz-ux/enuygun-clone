// Builds the destination autocomplete index from the live catalogue:
// distinct `region` values + their hotel counts → src/lib/destinations.data.ts
// Re-run after a fresh ingest. Read-only (anon key).
import { createClient } from "@supabase/supabase-js";
import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";

// load .env.local
try {
  const raw = readFileSync(".env.local", "utf8");
  for (const line of raw.split(/\r?\n/)) {
    const m = line.match(/^([A-Z0-9_]+)=(.*)$/);
    if (m && !process.env[m[1]]) process.env[m[1]] = m[2];
  }
} catch {}

const sb = createClient(
  process.env.NEXT_PUBLIC_SUPABASE_URL,
  process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY,
  { auth: { persistSession: false } },
);

const TR_FOLD = { İ: "i", I: "i", ı: "i", Ş: "s", ş: "s", Ğ: "g", ğ: "g", Ç: "c", ç: "c", Ö: "o", ö: "o", Ü: "u", ü: "u" };
const slugify = (s) =>
  s.trim().replace(/[İIıŞşĞğÇçÖöÜü]/g, (c) => TR_FOLD[c] ?? c).toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-+|-+$/g, "");

// Paginate the whole table (anon max-rows is 1000) and tally region counts.
const counts = new Map();
const PAGE = 1000;
for (let from = 0; ; from += PAGE) {
  const { data, error } = await sb
    .from("hotels")
    .select("region")
    .order("id")
    .range(from, from + PAGE - 1);
  if (error) { console.error(error); process.exit(1); }
  if (!data?.length) break;
  for (const r of data) {
    const name = (r.region ?? "").trim();
    if (name) counts.set(name, (counts.get(name) ?? 0) + 1);
  }
  if (data.length < PAGE) break;
}

const list = [...counts.entries()]
  .map(([name, count]) => ({ name, slug: slugify(name), count }))
  .filter((d) => d.slug.length > 0)
  .sort((a, b) => b.count - a.count || a.name.localeCompare(b.name, "tr"));

const out =
  "// AUTO-GENERATED by scripts/gen-destinations.mjs — do not edit by hand.\n" +
  "// Distinct hotel `region` values + hotel counts, for the search autocomplete.\n" +
  "export type Destination = { name: string; slug: string; count: number };\n\n" +
  "export const DESTINATIONS: Destination[] = " +
  JSON.stringify(list, null, 0).replace(/},/g, "},\n  ").replace(/^\[/, "[\n  ").replace(/\]$/, ",\n]") +
  ";\n";

const target = path.join("src", "lib", "destinations.data.ts");
writeFileSync(target, out, "utf8");
console.log(`✓ wrote ${list.length} destinations → ${target}`);
console.log("top 8:", list.slice(0, 8).map((d) => `${d.name}(${d.count})`).join(", "));
